		À l'époque du second congrès de mathématiques, tenu à Paris en 1900, Hilbert, professeur à
		l'université de Göttingen, avait songé à présenter une réponse à la conférence faite quatre ans
		auparavant par son grand rival, Poincaré, dans laquelle il plaidait pour des relations étroites
		entre les mathématiques et la physique. Mais Hilbert, sur une suggestion de son collègue Hermann
		Minkowski, choisit de tenter de « deviner le futur » des mathématiques, à travers un choix de
		problèmes. Felix Klein n'avait-il pas coutume de dire à ses étudiants que les mathématiques se
		développent « quand de vieux problèmes sont résolus par des méthodes nouvelles, et que
		l'approfondissement de ces questions anciennes fait naître en retour de nouveaux problèmes ».
		Aussi Hilbert, convaincu du rôle des grandes questions et de l'existence dans tous les cas d'une
		réponse, fit la liste des vingt-trois « problèmes » qui allaient, selon lui, marquer le cours
		des mathématiques du XXe siècle. Recherche en mathématiques, N. Berline Recherche en
		mathématiques, N. Berline Encyclopædia Universalis France Le lecteur qui connaît la diversité
		des domaines des mathématiques où Hilbert a laissé de profondes marques (algèbre, théorie des
		nombres, analyse, problèmes d'axiomatique et de fondements...) ne sera pas étonné d'apprendre
		que l'histoire de ces problèmes, des travaux qu'ils ont suscités ressemble beaucoup à l'histoire
		des mathématiques au XXe siècle ! Il est cependant étrange que les spécialités qui paraissent a
		posteriori insuffisamment représentées (topologie, analyse) sont celles où Hilbert devait faire,
		après 1900, ses travaux les plus importants ou celles où l'influence de Poincaré est restée
		dominante. Devant l'ampleur des travaux qu'on peut relier aux problèmes de 1900 posés par
		Hilbert, notre choix a été d'insister sur les questions dont l'importance montre le mieux
		l'extraordinaire intuition prospective du célèbre mathématicien, en évoquant seulement celles
		qui font l'objet d'autres développements dans l'Encyclopædia Universalis.
